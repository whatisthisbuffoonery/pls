int	ft_atoi(char *str)
{
	int	i;
	int	flag;
	int	a;

	i = 0;
	a = 0;
	flag = 0;
	while (str[i] == 32 || (str[i] > 8 && str[i] < 14))
		i ++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			flag ++;
		i ++;
	} 
	while (str[i] >= '0' && str[i] <= '9')
	{
		a *= 10;
		a = a + str[i] - '0';	
		i ++;
	}
	if (flag % 2 == 1)
		a = 0 - a;
	return (a);
}
