#!/usr/bin/env python3

a_str = input("Give me the first number: ")
b_str = input("Give me the second number: ")
print("Thank you!")
a = int(a_str)
b = int(b_str)
print(a_str + " + " + b_str + " = " + str(a + b))
print(a_str + " - " + b_str + " = " + str(a - b))
print(a_str + " / " + b_str + " = " + str(a / b))
print(a_str + " * " + b_str + " = " + str(a * b))
