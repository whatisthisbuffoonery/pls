int	ft_dtr_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < 65 || str[i] > 122)
		{
			return (0);
		}
		if (str[i] > 90 && str[i] < 97)
		{
			return (0);
		}
		i ++;
	}
	return (1);
}
